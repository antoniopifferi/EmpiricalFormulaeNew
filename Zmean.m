function [Z,t0]=Zmean(t,s0,musp,nin,nout,rho)

m  = -5000:5000;

t=t*1e-9;
musp=musp*1e3;
% z=z*1e-3;
s0=s0*1e-3;
rho=rho*1e-3;

% speed of light
c=299792458; % m s^(-1)
v=c/nin;

t0=rho/v*1e9;

D  = 1/(3*musp);
A  = Afunction(nout,nin);
zs=1/(musp); %MODIFICATO COME SOTTO PER PROVA
% zs=1/(musp+mua);

ze1=ones(size(m))*2*A*D;
ze2=ones(size(m))*2*D;

M  = -2*m.*ze1-2*m.*ze2-zs;
N  = -2*m.*ze2-(2*m-2).*ze1+zs;



fA=zeros(size(t));
fB=zeros(size(t));
for i=1:length(t)
    
    
    z30 = -2*m*s0 - 4*m.*ze1     -zs;
    z40 = -2*m*s0 - (4*m-2).*ze1 +zs;
    
    tmp=D*exp(-(1/4)*M.^2/(D*t(i)*v)).*t(i)*v./m-...
        D*exp(-(1/4)*N.^2/(D*t(i)*v)).*t(i)*v./m-...
        (2*m.^2*s0^2+D*t(i)*v-M.*m*s0).*exp(-(1/4)*(4*m.^2*s0^2-4*M.*m*s0+M.^2)/(D*t(i)*v))./m-...
        (-2*m.^2*s0^2-D*t(i)*v+N.*m*s0).*exp(-(1/4)*(4*m.^2*s0^2-4*N.*m*s0+N.^2)/(D*t(i)*v))./m;
    
    tmp(isnan(tmp))=0;
    fA(i)=sum(tmp);
    
    
    fB(i)=sum(z30.*exp(-z30.^2/(4*D*v*t(i))) - z40.*exp(-z40.^2/(4*D*v*t(i))));
    
end

Z=fA./fB;
Z(t==0)=0;
Z=Z*1e3;

end


