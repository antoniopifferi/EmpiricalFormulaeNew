function gZ=fgz(s0,mua,musp,nin,nout,M)

s0=s0*1e-3;
mua=mua*1e3;
musp=musp*1e3;

A=Afunction(nout,nin);
D=1/(3*musp);
mueff=sqrt(mua/D);
ze=2*A*D;
zep=2*D;
zs=1/(musp);
zeff=sqrt(3*mua*musp);

m=(-M:M);
m(m==0)=[];

MM  = -2*m.*ze-2*m.*zep-zs;
NN  = -2*m.*zep-(2*m-2).*ze+zs;
z30p=-2*m*s0+MM;
z40p=-2*m*s0+NN;

F=sign(z30p).*s0.*exp(-mueff*abs(z30p)) - exp(-mueff*abs(z30p))./(2*mueff*m) + exp(-mueff*abs(MM))./(2*mueff*m);
G=sign(z40p).*s0.*exp(-mueff*abs(z40p)) - exp(-mueff*abs(z40p))./(2*mueff*m) + exp(-mueff*abs(NN))./(2*mueff*m);

m=(-M:M);
z30 = -2*m*s0 - 4*m.*ze     -zs;
z40 = -2*m*s0 - (4*m-2).*ze +zs;
H=sign(z30).*exp(-mueff*abs(z30)) -sign(z40).*exp(-mueff*abs(z40));

gZ=sum((F-G)/sum(H));
gZ=gZ*1e3;

end

